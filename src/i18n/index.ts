export type TranslationKey = keyof TranslationDict;

export const translations: Record<Lang, TranslationDict> = { en, de, fa };

import { useAppearance } from '@/features/settings/appearanceStore';

export function useT() {
  const lang = (useAppearance(s => (s as { language?: string }).language) ?? 'en') as Lang;

  const t = (key: TranslationKey, vars?: Record<string, string | number>): string => {
    const dict = translations[lang] ?? translations.en;
    let str: string = (dict as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{{${k}}}`, String(v));
      });
    }
    return str;
  };

  return { t, lang, isRTL: lang === 'fa' };
}
